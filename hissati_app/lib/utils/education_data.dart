class EducationData {
  static List<String> get levels => [
        'ابتدائي',
        'إعدادي',
        'ثانوي',
        'جامعي',
      ];

  static List<String> subjectsFor(String level) {
    switch (level) {
      case 'ابتدائي':
        return [
          'رياضيات',
          'لغة عربية',
          'لغة إنجليزية',
          'علوم',
          'دراسات اجتماعية',
        ];
      case 'إعدادي':
        return [
          'رياضيات',
          'لغة عربية',
          'لغة إنجليزية',
          'علوم',
          'دراسات اجتماعية',
          'حاسب آلي',
        ];
      case 'ثانوي':
        return [
          'رياضيات',
          'فيزياء',
          'كيمياء',
          'أحياء',
          'لغة عربية',
          'لغة إنجليزية',
          'لغة فرنسية',
          'تاريخ',
          'جغرافيا',
          'فلسفة',
        ];
      case 'جامعي':
        return [
          'رياضيات',
          'فيزياء',
          'كيمياء',
          'أحياء',
          'برمجة',
          'هندسة',
          'طب',
          'محاسبة',
        ];
      default:
        return [];
    }
  }
}
